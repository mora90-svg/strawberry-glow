# قالب موقع ثابت (Static Site Template)

هذا قالب بداية لصفحة ثابتة متجاوبة وسهلة التخصي��.

التشغيل محلياً:
1. احفظ الملفات `index.html`, `styles.css`, `script.js` في مجلد.
2. افتح `index.html` مباشرة أو شغّل خادم بسيط:
   - Python 3: `python -m http.server 8000`
   - ثم افتح http://localhost:8000

النشر على GitHub Pages:
1. أنشئ مستودع جديد وادفع الملفات إليه.
2. في إعدادات المستودع (Settings) > Pages، اختر الفرع `main` أو `gh-pages` والمجلد `/ (root)`.
3. احفظ وسيصبح الموقع متاحاً عبر username.github.io/repo.

التخصيص السريع:
- لتغيير الألوان: عدّل متغيرات :root في `styles.css`.
- استبدل صور المعرض والـ hero بروابطك أو ملفاتك.
- لعمل نموذج حقيقي ربطه بخدمة مثل Formspree, Netlify Forms، أو API خاص.

إذا تريد، أستطيع:
- رفع هذه الملفات مباشرة لمستودع GitHub لديك.
- إضافة تصميم باللغتين (عربي/إنجليزي) أو تحويله إلى موقع متعدد الصفحات.
- تحسين SEO أو إضافة build بسيط بـ Vite/Eleventy.
